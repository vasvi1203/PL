/*Write a program that calculates how long it takes to drive from New York to Los Angeles at 75 mile per hour (Use 3000 miles as the approximate distance between two cities).*/

public class Time {
	public static void main(String[] args) {
		int time;
		time = 3000 / 75;
		System.out.println("Time it takes to drive from New York to Los Angeles is " + time + " hours");
	}
}
